# ค่าพยากรณ์ของปี 2561 (12 เดือน)
forecast_2561 <- c(
  134.98100, 97.93705, 115.83685, 146.35413, 101.37004, 88.66382,
  113.11106, 93.59086, 102.92853, 81.97390, 117.52335, 153.99790
)

# รวมข้อมูลจริงและค่าพยากรณ์
all_sales <- c(sales, forecast_2561)

# สร้างข้อมูล Time Series ครอบคลุมปี 2557–2561
all_sales_ts <- ts(all_sales, start = c(2557, 1), frequency = 12)

# แยกส่วนประกอบด้วย decompose
sales_components <- decompose(all_sales_ts, type = "multiplicative")

# แสดงส่วนประกอบต่าง ๆ
trend <- sales_components$trend
seasonal <- sales_components$seasonal
random <- sales_components$random

# พล็อตกราฟแยกส่วนประกอบ
plot(sales_components)

