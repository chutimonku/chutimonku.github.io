#ยอดขายแ่ละเดือนของแต่ละปี ตั้งแต่ปี2557-2560
# ติดตั้งและโหลดแพ็กเกจ ggplot2
if (!require(ggplot2)) install.packages("ggplot2")
library(ggplot2)

# สร้างข้อมูลยอดขาย
data <- data.frame(
  Month = rep(c("ม.ค.", "ก.พ.", "มี.ค.", "เม.ย.", "พ.ค.", "มิ.ย.", "ก.ค.", "ส.ค.", "ก.ย.", "ต.ค.", "พ.ย.", "ธ.ค."), 4),
  Year = rep(2557:2560, each = 12),
  Sales = c(c(110.5, 70.3, 95.3, 120.6, 90.5, 75.5, 69.6, 77.2, 99.5, 82.1, 101.2, 103.5), # 2557
            c(153.9, 89.1, 121.0, 148.8, 100.1, 95.5, 93.4, 88.9, 120.3, 92.2, 132.4, 121.3), # 2558
            c(152.0, 91.9, 111.2, 149.9, 99.0, 83.3, 109.1, 87.7, 104.1, 75.7, 123.3, 159.5), # 2559
            c(141.5, 93.6, 112.1, 152.3, 98.9, 85.5, 110.5, 88.2, 103.5, 76.5, 122.4, 162.2)  # 2560
           ))# ยอดขายสำหรับแต่ละปี

# เปลี่ยนลำดับของเดือน
data$Month <- factor(data$Month, levels = c("ม.ค.", "ก.พ.", "มี.ค.", "เม.ย.", "พ.ค.", "มิ.ย.", "ก.ค.", "ส.ค.", "ก.ย.", "ต.ค.", "พ.ย.", "ธ.ค."))

# Plot กราฟ
ggplot(data, aes(x = Month, y = Sales, group = Year, color = as.factor(Year))) +
  geom_line(size = 1) +
  geom_point(size = 2) +
  labs(
    title = "ยอดขายสินค้าคางหมึกยักษ์แช่แข็ง (พ.ศ. 2557–2560)",
    x = "เดือน",
    y = "ยอดขาย (ตัน)",
    color = "ปี"
  ) +
  theme_minimal() +
  theme(
    text = element_text(family = "sans", size = 12),
    plot.title = element_text(hjust = 0.5, face = "bold")
  )

